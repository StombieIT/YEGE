N = int(input()) # Кол-во элементов
mx2 = 0 # Максимальное кратное 2
mx5 = 0 # Максимальное кратное 5
for i in range(N):
	c = int(input()) # Элемент
	if c % 5 == 0 and c % 10 != 0 and c > mx5: # Если кратно 5, не кратно 10 и больше максимума 5
		mx5 = c
	elif c % 2 == 0 and c % 10 != 0 and c > mx2: # Если кратно 2, не кратно 10 и больше максимума 2
		mx2 = c
print(mx2, mx5)
